import numpy as np

class WindTurbine:
  ''' Wind turbine simulation.
  
  Args:
    n_turbines (:type:`int`): Number of wind turbines.
    rated_power (:type:`int | float`): Rated power of the wind turbines in [kW].
    rated_wind_speed (:type:`int | float`): The rated wind speed in [m/s].
    cut_in (:type:`int | float`): The cut in speed in [m/s].
    cut_out (:type:`int | float`): The cut out speed in [m/s].
    height (:type:`int | float`): The wind turbine hub height in [m].
    efficiency (:type:`int | float`): The wind turbine efficiency between 0 and 1.
    lifetime (:type:`int | float`): The wind turbine lifetime in [year].

  Raises:
    TypeError: If the input is not the expected type.
    ValueError: If the input is not the allowed value.
  '''

  def __init__(self,
               n_turbines: int,
               rated_power: int | float = 5,
               rated_wind_speed: int | float = 12.5,
               cut_in: int | float = 3,
               cut_out: int | float = 20,
               height: int | float = 30,
               efficiency: int | float = 0.95,
               lifetime: int | float = 24):
    
    self.n_turbines: int
    ''' Number of wind turbines. '''
    self.rated_power: int | float
    ''' Rated power of the wind turbine in [kW]. '''
    self.rated_wind_speed: int | float
    ''' The rated wind speed in [m/s]. '''
    self.cut_in: int | float
    ''' The cut in speed in [m/s]. '''
    self.cut_out: int | float
    ''' The cut out speed in [m/s]. '''
    self.height: int | float
    ''' The wind turbine hub height in [m]. '''
    self.efficiency: int | float
    ''' The wind turbines efficiency between 0 and 1. '''
    self.lifetime: int | float
    ''' The wind turbines lifetime in [year]. '''
    self.output_power: np.ndarray[np.float64] | None = None
    ''' Numpy array to store the output power generated by the wind turbines in kW. Default value is ``None``. '''

    self.n_turbines = n_turbines
    self.rated_power = rated_power
    self.rated_wind_speed = rated_wind_speed
    self.cut_in = cut_in
    self.cut_out = cut_out
    self.height = height
    self.efficiency = efficiency
    self.lifetime = lifetime
  
  def generate_power(self, wind_speed: np.ndarray[np.float64], height_reference: int | float) -> np.ndarray[np.float64]:
    r''' Generates the output power according to the following cubic interpolation model:

    .. math::

        P(v) =
        \begin{cases}
          0, & v < v_{ci}; \\
          P_r \cdot \left(\frac{v^3 - v_{ci}^3}{v_r^3 - v_{ci}^3}\right), & v_{ci} \leq v < v_r; \\
          P_r, & v_r \leq v \leq v_{co}; \\
          0, & v > v_{co}.
        \end{cases}

    where:

    - :math:`v` is the wind speed at the hub height in [m/s];
    - :math:`v_{ci}` is the cut-in wind speed in [m/s];
    - :math:`v_r` is the rated wind speed in [m/s];
    - :math:`v_{co}` is the cut-out wind speed in [m/s];
    - :math:`P_r` is the rated power in [kW].

    Args:
      wind_speed (:type:`np.ndarray[np.float64]`): A numpy array with the wind speed measurements at the reference height.
      height_reference (:type:`int | float`): The height where the wind speed was measured in [m].

    Returns:
      np.ndarray[np.float64]: The output power of the wind turbine in [kW].
    '''

    # Applicates the power law
    alpha = 1/7 # Hellman exponent assumed to be 1/7
    wind_speed_hub = wind_speed * (self.height/height_reference)**alpha # Estimate the wind speed in the hub
    # Calculate the output power using power curve
    self.output_power = np.zeros(len(wind_speed))
    mask = (self.cut_in <= wind_speed_hub) & (wind_speed_hub <= self.cut_out)
    turbine_power = self.rated_power * (wind_speed_hub[mask]**3 - self.cut_in**3)/(self.rated_wind_speed**3 - self.cut_in**3)
    self.output_power[mask] = self.n_turbines * np.minimum(turbine_power, self.rated_power)

    # if wind_direction is not None:
    #   theta = np.abs(wind_direction - self.turbine_direction)
    #   theta = np.deg2rad(theta)
    #   adjustment_factor = np.cos(theta) ** 3
    #   turbine_power *= adjustment_factor[mask]
    
    return self.output_power