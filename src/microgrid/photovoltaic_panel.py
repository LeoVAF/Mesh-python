import numpy as np

class PhotovoltaicPanel:
  ''' Class representing a photovoltaic panel in a microgrid system.
  
  Args:
    cost_per_kw (:type:`int | float`): Photovoltaic panels cost per kW maximum power.
    rated_power (:type:`int | float`): The maximum power output of the photovoltaic panels in [kW].
    lifetime (:type:`int | float`): The photovoltaic panels lifetime in [year].
  
  Raises:
    TypeError: If the input is not the expected type.
    ValueError: If the input is not the allowed value.
  '''

  def __init__(self,
               cost_per_kw: int | float,
               rated_power: int | float = 30,
               lifetime: int | float = 24):
    
    self.cost_per_kwh: int | float
    ''' Photovoltaic panel cost per kW maximum power. '''
    self.rated_power: int | float
    ''' Maximum power output of the photovoltaic panel in [kW]. '''
    self.lifetime: int | float
    ''' The photovoltaic panel lifetime in [year]. '''
    self.output_power: np.ndarray[np.float64] | None = None
    ''' Output power generated by the photovoltaic panels in [kW]. Default value is ``None``. '''

    self.cost_per_kwh = cost_per_kw
    self.rated_power = rated_power
    self.lifetime = lifetime

  def initialize(self, hour_steps: int) -> None:
    ''' Initializes the components of the photovoltaic panel.

    Args:
      hour_steps (:type:`int`): Number of hour steps in the simulation.
    '''

    self.output_power = np.zeros(hour_steps)

  def generate_power(self, temperature: np.ndarray[np.float64], solar_radiation: np.ndarray[np.float64]) -> None:
    r''' Generates the output power according to the following equation:
      
      .. math::
        P_{out} = P_r \cdot \left(\frac{G}{G_{ref}}\right) \cdot (1 + K_t \cdot (T_c - T_{ref})).

      where:
      
      - :math:`P_{out}` is the output power in [kW];
      - :math:`P_r` is the rater power under reference conditions in [kW];
      - :math:`G` is the solar radiation in [kW/m^2];
      - :math:`G_{ref}` is the solar radiation at reference conditions in [kW/m^2];
      - :math:`K_t` is the power temperature coefficient of the photovoltaic panels in [1/ºC];
      - :math:`T_{amb}` is the ambient temperature in [ºC];
      - :math:`T_c` is the cell temperature in [ºC] estimated according to the following equation, considering :math:`NOCT = 45`:

        .. math::
          T_c = T_{amb} + \frac{NOCT - 20}{800} \cdot G.

      - :math:`T_{ref}` is the temperature at reference conditions in [ºC].
    
      Args:
        temperature (np.ndarray[np.float64]): Numpy array with the temperature in [ºC] at the time.
        solar_radiation (np.ndarray[np.float64]): Numpy array with the solar radiation in [kW/m^2] at the time.
    '''

    irradiance_ref = 1 # Reference irradiance [kWh/m^2]
    temperature_ref = 25 # Reference temperature in [ºC]
    power_temperature_coefficient = 3.7e-3 # Power temperature coefficient of maximum power in [1/°C]
    cell_temperature = temperature + 0.03125 * solar_radiation # Cell temperature
    self.output_power[:] = np.minimum(self.rated_power * (solar_radiation/irradiance_ref) * (1 + power_temperature_coefficient*(cell_temperature - temperature_ref)), self.rated_power)